class TelecommunicationGermany:
    landline_area_codes = []
    landline_provider = []
    cellphone_provider = []
    service_provider = []

    def __init__(self):
        reader_area_codes = csv.DictReader(open('./src/SynData/utils/de_plz_ort_vorwahl.csv', 'r', encoding="utf-8"), delimiter=';')
        for row in reader_area_codes:
            self.landline_area_codes.append(row)
        reader_provider = csv.DictReader(open('./src/SynData/utils/de_vorwahl_betreiber.csv', 'r', encoding="utf-8"), delimiter=';')
        for row in reader_provider:
            self.landline_provider.append(row)
        reader_cell_provider = csv.DictReader(open('./src/SynData/utils/de_mobilfunk_betreiber.csv', 'r', encoding="utf-8"), delimiter=',')
        for row in reader_cell_provider:
            self.cellphone_provider.append(row)
        reader_service_provider = csv.DictReader(open('./src/SynData/utils/de_portierungskennungen.csv', 'r', encoding="utf-8"), delimiter=',')
        for row in reader_service_provider:
            self.service_provider.append(row)

    def add_cellphone_number(self, item, filter={}, information_path="communication.cellphone"):
        # create path inside item
        path_nodes = information_path.split('.')
        p_leaf = item
        for node in path_nodes:
            if None == p_leaf.get(node):
                p_leaf.update({node: {}})
            p_leaf = p_leaf.get(node)
        # select data set from filtered record set
        cellphone_set = self.__get_random_cellphone_set_by_filter(filter)
        # create instance, if possible
        if None != cellphone_set:
            min = int(str(cellphone_set.get("number_range_min")))
            max = int(str(cellphone_set.get("number_range_max")))
            p_leaf.update({"number": "+" + str(49) + " " + str(cellphone_set.get("service_id")) + str(cellphone_set.get("block_id")) + " " + str(random.randrange(min, max))})
            p_leaf.update({"provider": cellphone_set.get("provider")})
            if 5 < len(str(cellphone_set.get("former_provider"))):
                p_leaf.update({"former_provider": cellphone_set.get("former_provider")})
        return item

    def add_landline_number(self, item, filter={}, information_path="communication.landline"):
        # create path inside item
        path_nodes = information_path.split('.')
        p_leaf = item
        for node in path_nodes:
            if None == p_leaf.get(node):
                p_leaf.update({node: {}})
            p_leaf = p_leaf.get(node)
        # update filter in two steps
        # 1. Should the area code match with address?
        if None != item.get("address", None):
            if None == item.get("area_code", None):
                filter.update({"area_code" : self.__build_area_code_rule(item.get("address"))})
        # 2. Translate provider to Portienrungskennung
        if (None != filter.get("provider")) and (None == filter.get("provider_id")):
            # In this case the provider should be translated to a set of Portierungskennungen
            providers = self.__perform_string_filter(self.service_provider, "provider"
                                                                            "", filter, "provider")
            if 0 < len(providers):
                provider_ids = []
                for it in providers:
                    provider_ids.append(it.get("Portierungskennung"))
                filter.update({"provider_id":{"operator":"set", "value": provider_ids}})

        landline_set = self.__get_random_landline_set_by_filter(filter)
        if None != landline_set:
            min = int(str(landline_set.get("RufnummernblockVon")))
            max = min + int(str(landline_set.get("RNBGroesse")))
            p_leaf.update({"number": "+" + str(49) + " " + str(landline_set.get("Ortsnetzkennzahl")) + " " + str(random.randrange(min, max))})
            p_leaf.update({"provider_id": landline_set.get("Portierungskennung")})
            p_leaf.update({"provider": self.__get_provider(landline_set.get("Portierungskennung"))})

        return item

    def __get_random_cellphone_set_by_filter(self, filter):
        retval = None
        retval_set = self.cellphone_provider.copy()
        for key in filter.keys():
            if key in ["provider"]:
                retval_set = self.__perform_string_filter(retval_set, key, filter, key)
        if 0 < len(retval_set):
            retval = retval_set[random.randrange(0, len(retval_set))]
        return retval

    def __get_random_landline_set_by_filter(self, filter):
        retval = None
        if None == filter.get("area_code", None):
            subset = self.landline_provider.copy()
        else:
            subset = self.__perform_string_filter(self.landline_provider,"Ortsnetzkennzahl", filter, "area_code")
        for key in filter.keys():
            if "provider_id" == key:
                subset = self.__perform_string_filter(subset, "Portierungskennung", filter, "provider_id")
        if 0 < len(subset):
            retval = subset[random.randrange(0, len(subset))]
        return retval

    def __build_area_code_rule(self, adress_item):
        retval = {}
        filter = {"postcode":{"operator":"equals", "value": adress_item.get("postcode")}}
        subset = self.__perform_string_filter(self.landline_area_codes, "postcode", filter, "postcode")
        if 1 < len(subset):
            filter = {"city":{"operator":"contains", "value": str(adress_item.get("city")).split(" ")[0]}}
            subset = self.__perform_string_filter(subset, "city", filter, "city")
        if 1 == len(subset):
            match = subset[0]
            retval.update({"operator":"equals"})
            retval.update( {"value": str(match.get("area_code")).lstrip("0") })
        return retval


    def __perform_string_filter(self, subset, key_subset, filter, key_filter):
        operator = str(filter.get(key_filter, {}).get("operator", "undefined")).lower()
        value = str(filter.get(key_filter, {}).get("value", ""))
        retval = []
        for item in subset:
            if ("regex" == operator):
                if (re.search(value, item.get(key_subset))):
                    retval.append(item)
            elif ("contains" == operator):
                if (-1 < item.get(key_subset).find(value)):
                    retval.append(item)
            elif ("equals" == operator):
                if (value == item.get(key_subset)):
                    retval.append(item)
            elif ("set" == operator):
                if (item.get(key_subset) in filter.get(key_filter).get("value")):
                    retval.append(item)
            else:
                retval.append(item)
        return retval

    def __get_provider(self, portierungskennung):
        retval = "unbekannt"
        subset = self.__perform_string_filter(self.service_provider, "Portierungskennung", {"id":{"operator":"equals", "value": portierungskennung}}, "id")
        if 1 == len(subset):
            retval = subset[0].get("provider", "unbekannt")
        return retval