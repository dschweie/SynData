class MailingAddressGermany:
    cities = []

    def __init__(self):
        reader = csv.DictReader(open('./src/SynData/utils/de_plz_ort_landkreis.csv', 'r', encoding="utf-8"))
#        reader = csv.DictReader(open('./de_plz_ort_landkreis.csv', 'r', encoding="utf-8"))
        for row in reader:
            self.cities.append(row)

    def add_location(self, person_data, address_attribute="address"):
        return self.add_filtered_location(person_data, {}, address_attribute)

    def add_filtered_location(self, person_data, filter={}, address_attribute="address"):
        #   First the given data set of cities will be filtered
        filter_keys = filter.keys()
        locations = self.get_locations_filtered_by_postcode(self.cities, filter)
        if ("city" in filter_keys):
            locations = self.get_locations_filtered_by_city(locations, filter)
        if ("county" in filter_keys):
            locations = self.get_locations_filtered_by_county(locations, filter)
        if ("state" in filter_keys):
            locations = self.get_locations_filtered_by_state(locations, filter)
        if (0 < len(locations)):
            #   In this case there is minimum one city in the result set
            streets = self.__get_streets_filtered(filter)
            if (0 < len(streets)):
                # In this case there is minimum one street in the result set
                person_data[address_attribute] = {}
                person_data[address_attribute]["street"] = streets[random.randrange(0, len(streets))]
                person_data[address_attribute]["housenumber"] = self.__get_random_housenumber_by_filter(filter)
                person_data[address_attribute].update(locations[random.randrange(0, len(locations))])

        return person_data

    def get_locations_filtered_by_postcode(self, locations, filter):
        min = int(filter.get("postcode", {}).get("minimum", "01000"))
        max = int(filter.get("postcode", {}).get("maximum", "99999"))
        retval = []
        for item in locations:
            if ((min <= int(item.get("postcode", "0"))) and (max >= int(item.get("postcode", "0")))):
                retval.append(item)
        return retval

    def get_locations_filtered_by_city(self, locations, filter):
        return self.__get_locations_filtered_by_key_for_string("city", "city", locations, filter)

    def get_locations_filtered_by_county(self, locations, filter):
        return self.__get_locations_filtered_by_key_for_string("county", "county", locations, filter)

    def get_locations_filtered_by_state(self, locations, filter):
        return self.__get_locations_filtered_by_key_for_string("state", "state", locations, filter)

    def __get_locations_filtered_by_key_for_string(self, key, key_filter, locations, filter):
        operator = str(filter.get(key_filter, {}).get("operator", "undefined")).lower()
        value = str(filter.get(key_filter, {}).get("value", ""))
        retval = []
        for item in locations:
            if ("regex" == operator):
                if (re.search(value, item.get(key))):
                    retval.append(item)
            elif ("contains" == operator):
                if (-1 < item.get(key).find(value)):
                    retval.append(item)
            elif ("equals" == operator):
                if (value == item.get(key)):
                    retval.append(item)
            else:
                retval.append(item)

        return retval

    def __get_streets_filtered(self, filter):
        operator = str(filter.get("street", {}).get("operator", "undefined")).lower()
        value = str(filter.get("street", {}).get("value", ""))
        retval = []
        for item in self.street_names:
            if ("regex" == operator):
                if (re.search(value, item)):
                    retval.append(item)
            elif ("contains" == operator):
                if (-1 < item.find(value)):
                    retval.append(item)
            elif ("equals" == operator):
                if (value == item):
                    retval.append(item)
            else:
                retval.append(item)
        return retval

    def __get_random_housenumber_by_filter(self, filter):
        min = int(filter.get("housenumber", {}).get("minimum", "1"))
        max = int(filter.get("housenumber", {}).get("maximum", "51"))
        mode = str(filter.get("housenumber", {}).get("mode", "any"))
        if ("numeric" == mode):
            retval = str(random.randrange(min, max))
        elif ("extended" == mode):
            extensions = ["a", "b", "a", "b", "c", "a", "b", "c", "d", "a" "b", "c", "d", "e", "a", "b", "c", "d", "e",
                          "f"]
            retval = str(random.randrange(min, max)) + extensions[random.randrange(0, len(extensions))]
        else:
            retval = str(random.randrange(min, max)) + self.housenumber_extensions[
                random.randrange(0, len(self.housenumber_extensions))]
        if 0 == random.randrange(0, 2):
            retval = retval.upper()
        return retval

